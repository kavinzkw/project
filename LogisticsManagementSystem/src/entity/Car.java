package entity;

public class Car {
  private String cid;
  private String starts;
  private String belong_rid;
  private String big_traffic;
  private String big_volume;
  private String place_now;
public String getCid() {
	return cid;
}
public void setCid(String cid) {
	this.cid = cid;
}
public String getStarts() {
	return starts;
}
public void setStarts(String starts) {
	this.starts = starts;
}
public String getBelong_rid() {
	return belong_rid;
}
public void setBelong_rid(String belong_rid) {
	this.belong_rid = belong_rid;
}
public String getBig_traffic() {
	return big_traffic;
}
public void setBig_traffic(String big_traffic) {
	this.big_traffic = big_traffic;
}
public String getBig_volume() {
	return big_volume;
}
public void setBig_volume(String big_volume) {
	this.big_volume = big_volume;
}
public String getPlace_now() {
	return place_now;
}
public void setPlace_now(String place_now) {
	this.place_now = place_now;
}
  
  
}
