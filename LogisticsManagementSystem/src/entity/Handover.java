package entity;

public class Handover {
  private String hoid;
  private String ssid;
  private String uid;
  private String fuid;
  private String ho_data;
  private String oid;
  
public String getFuid() {
	return fuid;
}
public void setFuid(String fuid) {
	this.fuid = fuid;
}
public String getHoid() {
	return hoid;
}
public void setHoid(String hoid) {
	this.hoid = hoid;
}
public String getSsid() {
	return ssid;
}
public void setSsid(String ssid) {
	this.ssid = ssid;
}
public String getUid() {
	return uid;
}
public void setUid(String uid) {
	this.uid = uid;
}
public String getHo_data() {
	return ho_data;
}
public void setHo_data(String ho_data) {
	this.ho_data = ho_data;
}
public String getOid() {
	return oid;
}
public void setOid(String oid) {
	this.oid = oid;
}
   
}
