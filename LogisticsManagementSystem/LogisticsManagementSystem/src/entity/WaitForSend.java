package entity;

public class WaitForSend {
   private String oid;
   private String sname;
   private String category;
   private String send_data;
   private String Surplus_data;
public String getOid() {
	return oid;
}
public void setOid(String oid) {
	this.oid = oid;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public String getCategory() {
	return category;
}
public void setCategory(String category) {
	this.category = category;
}
public String getSend_data() {
	return send_data;
}
public void setSend_data(String send_data) {
	this.send_data = send_data;
}
public String getSurplus_data() {
	return Surplus_data;
}
public void setSurplus_data(String surplus_data) {
	Surplus_data = surplus_data;
}
   
}
